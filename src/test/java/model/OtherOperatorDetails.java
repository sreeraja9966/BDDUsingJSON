package model;

public class OtherOperatorDetails {
    private String otherOperatorCustNo;
    private String otherOperatorAs;
    private String authSignatureReq;
    
    public String getOtherOperatorCustNo() {
        return otherOperatorCustNo;
    }
    
    public void setOtherOperatorCustNo(String otherOperatorCustNo) {
        this.otherOperatorCustNo = otherOperatorCustNo;
    }
    
    public String getOtherOperatorAs() {
        return otherOperatorAs;
    }
    
    public void setOtherOperatorAs(String otherOperatorAs) {
        this.otherOperatorAs = otherOperatorAs;
    }
    
    public String getAuthSignatureReq() {
        return authSignatureReq;
    }
    
    public void setAuthSignatureReq(String authSignatureReq) {
        this.authSignatureReq = authSignatureReq;
    }
    
    @Override
    public String toString() {
        return "OtherOperatorDetails [otherOperatorCustNo=" + otherOperatorCustNo + ", otherOperatorAs="
                + otherOperatorAs + ", authSignatureReq=" + authSignatureReq + "]";
    }
    
}
