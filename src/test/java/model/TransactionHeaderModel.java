package model;

public class TransactionHeaderModel {
    private String parentVoucherTransAmount;
    private String childVoucherTransAmount;
    
    public String getParentVoucherTransAmount() {
        return parentVoucherTransAmount;
    }
    
    public void setParentVoucherTransAmount(String parentVoucherTransAmount) {
        this.parentVoucherTransAmount = parentVoucherTransAmount;
    }
    
    public String getChildVoucherTransAmount() {
        return childVoucherTransAmount;
    }
    
    public void setChildVoucherTransAmount(String childVoucherTransAmount) {
        this.childVoucherTransAmount = childVoucherTransAmount;
    }
    
    @Override
    public String toString() {
        return "TransactionHeaderModel [parentVoucherTransAmount=" + parentVoucherTransAmount
                + ", childVoucherTransAmount=" + childVoucherTransAmount + "]";
    }
    
}
